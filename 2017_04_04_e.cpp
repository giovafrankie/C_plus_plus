#include <iostream>

using namespace std;

main(){
	
	int a = 8;
	int b = 3;
	int c = 5;
	
	cout << "a = " << a;
	cout << "\nb = " << b;
	cout << "\nc = " << c;
	
	cout << "\n\na+b+c = " << a+b+c;
	cout << "\na-b*c = " << a-b*c;
	cout << "\na%b = " << a%b;
	cout << "\na*a+b*c = " << a*a+b*c;
	cout << "\n(a+a)*b = " << (a+a)*b;
	
	return 0;
	
}
